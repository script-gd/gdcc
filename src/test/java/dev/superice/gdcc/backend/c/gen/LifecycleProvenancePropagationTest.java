package dev.superice.gdcc.backend.c.gen;

import dev.superice.gdcc.backend.CodegenContext;
import dev.superice.gdcc.backend.ProjectInfo;
import dev.superice.gdcc.backend.c.gen.insn.DestructInsnGen;
import dev.superice.gdcc.backend.c.gen.insn.OwnReleaseObjectInsnGen;
import dev.superice.gdcc.enums.GodotVersion;
import dev.superice.gdcc.enums.LifecycleProvenance;
import dev.superice.gdcc.exception.InvalidInsnException;
import dev.superice.gdcc.gdextension.ExtensionAPI;
import dev.superice.gdcc.lir.LirBasicBlock;
import dev.superice.gdcc.lir.LirClassDef;
import dev.superice.gdcc.lir.LirFunctionDef;
import dev.superice.gdcc.lir.insn.DestructInsn;
import dev.superice.gdcc.lir.insn.TryOwnObjectInsn;
import dev.superice.gdcc.scope.ClassRegistry;
import dev.superice.gdcc.type.GdObjectType;
import dev.superice.gdcc.type.GdStringType;
import dev.superice.gdcc.type.GdVoidType;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import java.nio.file.Path;
import java.util.List;
import java.util.Map;

import static org.junit.jupiter.api.Assertions.assertDoesNotThrow;
import static org.junit.jupiter.api.Assertions.assertInstanceOf;
import static org.junit.jupiter.api.Assertions.assertThrows;

public class LifecycleProvenancePropagationTest {
    @Test
    @DisplayName("DestructInsnGen should reject AUTO_GENERATED outside __finally__")
    void destructInsnGenShouldRejectAutoGeneratedOutsideFinally() {
        var clazz = new LirClassDef("Worker", "RefCounted", false, false, Map.of(), List.of(), List.of(), List.of());
        var func = new LirFunctionDef("destruct_guard");
        func.setReturnType(GdVoidType.VOID);
        func.createAndAddVariable("s", GdStringType.STRING);
        var block = new LirBasicBlock("entry");
        var insn = new DestructInsn("s", LifecycleProvenance.AUTO_GENERATED);
        block.instructions().add(insn);
        func.addBasicBlock(block);
        func.setEntryBlockId("entry");

        var builder = newBodyBuilder(clazz, func, false);
        builder.setCurrentPosition(block, 0, insn);

        var ex = assertThrows(InvalidInsnException.class, () -> new DestructInsnGen().generateCCode(builder));
        assertInstanceOf(InvalidInsnException.class, ex);
    }

    @Test
    @DisplayName("DestructInsnGen should allow AUTO_GENERATED inside __finally__")
    void destructInsnGenShouldAllowAutoGeneratedInsideFinally() {
        var clazz = new LirClassDef("Worker", "RefCounted", false, false, Map.of(), List.of(), List.of(), List.of());
        var func = new LirFunctionDef("destruct_ok");
        func.setReturnType(GdVoidType.VOID);
        func.createAndAddVariable("s", GdStringType.STRING);
        var block = new LirBasicBlock("__finally__");
        var insn = new DestructInsn("s", LifecycleProvenance.AUTO_GENERATED);
        block.instructions().add(insn);
        func.addBasicBlock(block);
        func.setEntryBlockId("__finally__");

        var builder = newBodyBuilder(clazz, func, false);
        builder.setCurrentPosition(block, 0, insn);

        assertDoesNotThrow(() -> new DestructInsnGen().generateCCode(builder));
    }

    @Test
    @DisplayName("OwnReleaseObjectInsnGen should reject AUTO_GENERATED provenance")
    void ownReleaseInsnGenShouldRejectAutoGenerated() {
        var clazz = new LirClassDef("Worker", "RefCounted", false, false, Map.of(), List.of(), List.of(), List.of());
        var func = new LirFunctionDef("own_guard");
        func.setReturnType(GdVoidType.VOID);
        func.createAndAddVariable("obj", new GdObjectType("Object"));
        var block = new LirBasicBlock("entry");
        var insn = new TryOwnObjectInsn("obj", LifecycleProvenance.AUTO_GENERATED);
        block.instructions().add(insn);
        func.addBasicBlock(block);
        func.setEntryBlockId("entry");

        var builder = newBodyBuilder(clazz, func, false);
        builder.setCurrentPosition(block, 0, insn);

        var ex = assertThrows(InvalidInsnException.class, () -> new OwnReleaseObjectInsnGen().generateCCode(builder));
        assertInstanceOf(InvalidInsnException.class, ex);
    }

    private static CBodyBuilder newBodyBuilder(LirClassDef clazz, LirFunctionDef func, boolean strictMode) {
        var api = new ExtensionAPI(null, List.of(), List.of(), List.of(), List.of(), List.of(), List.of(), List.of(), List.of());
        var classRegistry = new ClassRegistry(api);
        classRegistry.addGdccClass(clazz);
        ProjectInfo projectInfo = new ProjectInfo("TestProject", GodotVersion.V451, Path.of(".")) {
        };
        var ctx = new CodegenContext(projectInfo, classRegistry, strictMode);
        var helper = new CGenHelper(ctx, List.of(clazz));
        return new CBodyBuilder(helper, clazz, func);
    }
}
